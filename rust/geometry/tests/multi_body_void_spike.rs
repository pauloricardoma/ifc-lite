fn void_index(content: &str) -> FxHashMap<u32, Vec<u32>> {
    let mut idx: FxHashMap<u32, Vec<u32>> = FxHashMap::default();
    let mut scanner = EntityScanner::new(content);
    let mut decoder = EntityDecoder::new(content);
    while let Some((id, name, start, end)) = scanner.next_entity() {
        if name == "IFCRELVOIDSELEMENT" {
            if let Ok(entity) = decoder.decode_at_with_id(id, start, end) {
                if let (Some(host), Some(opening)) = (entity.get_ref(4), entity.get_ref(5)) {
                    idx.entry(host).or_default().push(opening);
                }
            }
        }
    }
    let _ = propagate_voids_to_parts(&mut idx, content, &mut decoder);
    idx
}

fn process(content: &str, use_voids: bool) -> Mesh {
    let ei = build_entity_index(content);
    let mut decoder = EntityDecoder::with_index(content, ei);
    let entity = decoder.decode_by_id(WALL_ID).expect("decode wall");
    let router = GeometryRouter::with_units(content, &mut decoder);
    if use_voids {
        let idx = void_index(content);
        router
            .process_element_with_voids(&entity, &mut decoder, &idx)
            .expect("process with voids")
    } else {
        router.process_element(&entity, &mut decoder).expect("process")
    }
}

fn aabb(m: &Mesh) -> ([f32; 3], [f32; 3]) {
    let (lo, hi) = m.bounds();
    ([lo.x, lo.y, lo.z], [hi.x, hi.y, hi.z])
}

#[test]
fn multi_body_void_cut_does_not_spike_out_of_the_wall() {
    let base = process(MODEL, false);
    assert!(!base.is_empty(), "base wall mesh empty");
    let (blo, bhi) = aabb(&base);

    let voided = process(MODEL, true);
    assert!(!voided.is_empty(), "voided wall mesh empty");
    let (vlo, vhi) = aabb(&voided);

    // The cut must stay within the host bounds (a generous 0.1 m tolerance for
    // kernel snap / f64->f32 jitter; the reported spike protruded ~2 m).
    const TOL: f32 = 0.1;
    for k in 0..3 {
        let under = blo[k] - vlo[k];
        let over = vhi[k] - bhi[k];
        assert!(
            under <= TOL && over <= TOL,
            "voided wall spikes beyond host bounds on axis {k}: \
             base [{:.3}, {:.3}] vs voided [{:.3}, {:.3}] (under={under:.3} over={over:.3})",
            blo[k], bhi[k], vlo[k], vhi[k]
        );
    }

    // Sanity: voids actually cut something (fewer-or-equal solid, more tris).
    assert!(
        voided.triangle_count() > base.triangle_count(),
        "voids did not cut the wall (base {} tris, voided {} tris)",
        base.triangle_count(),
        voided.triangle_count()
    );
}
