//! Regression for the multi-body / tessellated void-cutter SPIKE.
//!
//! Reported: a wall with several window openings rendered a long needle
//! triangle poking ~2 m out of the wall near a cutout. Root cause: one opening
//! is a TESSELLATED `IfcPolygonalFaceSet` whose authored point list carries
//! garbage vertices ~9 m from the real window (a self-intersecting cutter with
//! huge "fin" faces), and a sibling opening is a 4-body `IfcExtrudedAreaSolid`.
//! Each cutter subtracts cleanly ON ITS OWN, but once BOTH are present the exact
//! mesh-arrangement leaks one of the malformed cutter's far-flung fin triangles
//! into the host output — a visible spike that slips past the per-cutter
//! admission guards (it only appears under the multi-cutter arrangement).
//!
//! A boolean subtract can only REMOVE material, so the cut is mathematically
//! contained in the host's pre-cut AABB; any output vertex beyond it is provably
//! an artifact. `apply_void_context_inner` drops such triangles
//! (`Mesh::clip_triangles_to_aabb`). This test asserts the voided wall does not
//! protrude beyond the un-voided wall's bounds.
//!
//! The SAME malformed cutters also make the kernel UNDER-cut — a wall flap
//! bridges the opening (the #1007 family). That is fixed by `clip_opening_flaps`
//! (rebuild each malformed cutter's real opening box, drop wall faces inside its
//! cross-section); its primitives are unit-tested in `router::voids` and the fix
//! is browser-verified, since a faithful flap detector needs the production
//! frame (the cutter's PCA box) — an independent frame can't localize the flaps
//! reliably.
//!
//! Fixture: the EXACT wall + all five triggering openings transcribed from the
//! reporter's file (synthetic `ifz_*` identifiers, no third-party data), wrapped
//! in a minimal project + millimetre unit assignment.

